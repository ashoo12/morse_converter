user_text=input("Enter your text here and i will convert it into morse code.\n").upper()
morse_code=''
for letter in user_text:
    for character in morse_code_dict:
        code=morse_code_dict[character]
        if letter == character:
           morse_code+= code+','


print(f'Morse code for your text is {morse_code}')

